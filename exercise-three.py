# مرتضی کیانی تمرین سوم

# گرفتن عدد دو رقمی از کاربر
num = input("لطفاً یک عدد دو رقمی وارد کنید: ")

# تبدیل هر رقم به عدد صحیح
digit1 = int(num[0])  # رقم اول
digit2 = int(num[1])  # رقم دوم

# محاسبه رقم اول به توان رقم دوم
power1 = digit1 ** digit2

# محاسبه رقم دوم به توان رقم اول
power2 = digit2 ** digit1

# نمایش نتایج
print("رقم اول به توان رقم دوم:", power1)
print("رقم دوم به توان رقم اول:", power2)
